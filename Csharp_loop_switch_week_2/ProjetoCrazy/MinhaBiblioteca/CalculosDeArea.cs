namespace MinhaBiblioteca
{
    public class CalculosDeArea
    {
        /// <summary>
        /// metodo retorna a area de um quadrado
        /// </summary>
        /// <param name="ladoDoQuadrado">lado do quadrado com a precisao inteira</param>
        /// <returns>retorna a area total do quadrado </returns>
        public double CalculaAreaDoQuadrado(double ladoDoQuadrado)
        {
            return ladoDoQuadrado * ladoDoQuadrado;
        }
    }
}
